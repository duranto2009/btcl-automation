package common;

public class NotificationInformation {
	String clientName ;
	String billingMailAddress;
	String billingMobileNumber;
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public String getBillingMailAddress() {
		return billingMailAddress;
	}
	public void setBillingMailAddress(String billingMailAddress) {
		this.billingMailAddress = billingMailAddress;
	}
	public String getBillingMobileNumber() {
		return billingMobileNumber;
	}
	public void setBillingMobileNumber(String billingMobileNumber) {
		this.billingMobileNumber = billingMobileNumber;
	}
	
}
